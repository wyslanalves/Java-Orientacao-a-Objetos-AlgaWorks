package orientacao_a_objetos_02.parte2;

public class PessoaBean {

    private String nome;
    private int idade;

    public PessoaBean(){

    }

    //setter e getter

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
