package fundamentos_da_linguagem_01;

public class OutrosOperadoresDeAtribuição {
    public static void main(String[] args) {

        int total = 10;

        //total = total + 3;
        total += + 3;
        System.out.println(total);
        total -= + 3;
        System.out.println(total);
        total *= + 3;
        System.out.println(total);
        total /= + 3;
        System.out.println(total);
        total %= + 3;
        System.out.println(total);
    }
}
