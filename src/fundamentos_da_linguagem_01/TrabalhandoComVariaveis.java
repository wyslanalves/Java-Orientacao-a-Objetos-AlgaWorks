package fundamentos_da_linguagem_01;

public class TrabalhandoComVariaveis {
    public static void main(String[] args) {

        int quantidade; //declarando a variavel inteiro
        quantidade = 10; // aqui estou atribuindo o valor 10

        System.out.println(quantidade);

        quantidade = 15; // trocando o valor

        System.out.println(quantidade);

    }
}
